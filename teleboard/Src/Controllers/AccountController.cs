        [AllowAnonymous]
        public async Task<ActionResult> ConfirmEmail(string userId, string code)
        {
            if (userId == null || code == null)
            {
                return View("Error");
            }
            var result = await UserManager.ConfirmEmailAsync(userId, code);
            return View(result.Succeeded ? "ConfirmEmail" : "Error");
        }

        [AllowAnonymous]
        public ActionResult ForgotPassword()
        {
            return View(new ForgotPasswordViewModel());
        }

        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        [ValidateModel]
        public async Task<ActionResult> ForgotPassword(ForgotPasswordViewModel model)
        {
            model.ShowEmailSentMessage = true;
            var user = await UserManager.FindByNameAsync(model.Email);
            model.Email = "";
            if (user == null || !(await UserManager.IsEmailConfirmedAsync(user.Id)))
            {
                // Don't reveal that the user does not exist or is not confirmed
                return View(model);
            }
            var callbackUrl = Url.Action("ResetPassword", "Account", new
            {
                UserId = user.Id,
                code = await UserManager.GeneratePasswordResetTokenAsync(user.Id)
            }, protocol: Request.Url.Scheme);
            await UserManager.SendEmailAsync(user.Id, Resources.ResetPassword, string.Format(Resources.ResetPasswordEmailBody, callbackUrl));
            return View(model);
        }

        [AllowAnonymous]
        public ActionResult ResetPassword(string code)
        {
            return View(new ResetPasswordViewModel()
            {
                Code = code,
            });
        }

        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        [ValidateModel]
        public async Task<ActionResult> ResetPassword(ResetPasswordViewModel model)
        {
            var user = await UserManager.FindByNameAsync(model.Email);
            if (user == null)
            {
                model.IsInvalidEmail = true;
                return View(model);
            }

            var resetResult = await UserManager.ResetPasswordAsync(user.Id, model.Code, model.Password);
            if (!resetResult.Succeeded)
            {
                model.IsInvalidCode = true;
                return View(model);
            }

            return View("ResetPasswordConfirmation"); 
        }

